"""
运动方案推荐工具
"""
from langchain_core.tools import tool
import logging

logger = logging.getLogger(__name__)

# 内置运动方案库
_EXERCISE_DB = {
    "减肥": {
        "原则": [
            "以有氧运动为主（60-70%）",
            "结合力量训练（30-40%）",
            "每次30-60分钟，每周5-6次",
        ],
        "推荐运动": [
            "快走：适合大体重初练者，不伤膝盖",
            "游泳：全身运动，对关节零压力",
            "椭圆机：低冲击有氧，适合膝盖不好的人",
            "动感单车：燃脂效率高，有趣味",
            "跳绳：高效燃脂（体重过大慎选）",
        ],
        "注意事项": "体重超过 90kg 避免跑步、跳跃等高冲击运动，优先选择游泳和椭圆机",
    },
    "增肌": {
        "原则": [
            "以力量训练为主",
            "每次40-60分钟，每周4-5次",
            "保证肌肉恢复时间（每个肌群48小时）",
        ],
        "推荐运动": [
            "深蹲：下肢核心训练",
            "卧推：胸部+手臂",
            "硬拉：全身力量提升",
            "引体向上：背部+二头肌",
            "卷腹：核心肌群",
        ],
        "注意事项": "新手建议从固定器械开始，避免自由重量过大导致受伤",
    },
    "保持": {
        "原则": [
            "有氧+力量平衡（各50%）",
            "每次30分钟，每周3-4次",
        ],
        "推荐运动": [
            "慢跑或快走30分钟",
            "瑜伽/普拉提：提升柔韧性",
            "徒手训练：俯卧撑、深蹲、平板支撑",
            "羽毛球/乒乓球：趣味性强，容易坚持",
        ],
        "注意事项": "关键是坚持，找到自己喜欢的运动才能长期维持",
    },
}


@tool
def get_exercise_plan(goal: str, bmi: float = 0) -> str:
    """
    获取运动方案推荐：根据用户目标（减肥/增肌/保持）和BMI给出运动计划。
    输入示例：goal=减肥, bmi=35.5
    """
    goal = goal.strip()
    if goal not in _EXERCISE_DB:
        goals = ", ".join(_EXERCISE_DB.keys())
        return f"当前支持的目标：{goals}，请输入其中之一"

    info = _EXERCISE_DB[goal]
    result = f"=== {goal}运动方案 ===\n"

    result += f"\n📋 训练原则：\n" + "\n".join(f"  · {p}" for p in info["原则"])

    result += f"\n\n🏃 推荐运动：\n" + "\n".join(f"  · {e}" for e in info["推荐运动"])

    result += f"\n\n⚠️ 注意事项：{info['注意事项']}"

    if bmi > 28 and goal == "减肥":
        result += "\n\n💡 大体重专属建议：优先选择游泳、椭圆机、水中运动，避免跑步、跳绳"

    logger.info("运动方案生成|goal=%s", goal)
    return result
